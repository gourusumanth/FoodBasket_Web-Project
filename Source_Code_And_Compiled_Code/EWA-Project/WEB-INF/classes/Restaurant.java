
public class Restaurant {

    private String id;

    private String name;

    private String tagLine;

    private String time;

    private String expensiveMeter;

    private float minimumAmount;

    private String image;

    private float deliverFee;

    private String zipCode;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTagLine() {
        return tagLine;
    }

    public void setTagLine(String tagLine) {
        this.tagLine = tagLine;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getExpensiveMeter() {
        return expensiveMeter;
    }

    public void setExpensiveMeter(String expensiveMeter) {
        this.expensiveMeter = expensiveMeter;
    }

    public float getMinimumAmount() {
        return minimumAmount;
    }

    public void setMinimumAmount(float minimumAmount) {
        this.minimumAmount = minimumAmount;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public float getDeliverFee() {
        return deliverFee;
    }

    public void setDeliverFee(float deliverFee) {
        this.deliverFee = deliverFee;
    }

    public String getZipCode() {
        return zipCode;
    }

    public void setZipCode(String zipCode) {
        this.zipCode = zipCode;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
